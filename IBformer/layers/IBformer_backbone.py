class IBformer_backbone(nn.Module):
    def __init__(self, c_in: int, context_window: int, target_window: int, patch_len: int, stride: int,
                 max_seq_len: Optional[int] = 1024,
                 n_layers: int = 3, d_model=128, n_heads=16, d_k: Optional[int] = None, d_v: Optional[int] = None,
                 d_ff: int = 256, norm: str = 'BatchNorm', attn_dropout: float = 0., dropout: float = 0.,
                 act: str = "gelu", key_padding_mask: bool = 'auto',
                 padding_var: Optional[int] = None, attn_mask: Optional[Tensor] = None, res_attention: bool = True,
                 pre_norm: bool = False, store_attn: bool = False,
                 pe: str = 'zeros', learn_pe: bool = True, fc_dropout: float = 0., head_dropout=0, padding_patch=None,
                 pretrain_head: bool = False, head_type='flatten', individual=False, revin=True, affine=True,
                 subtract_last=False,
                 verbose: bool = False, **kwargs):

        super().__init__()

        # RevIn
        self.revin = revin
        if self.revin: self.revin_layer = RevIN(c_in, affine=affine, subtract_last=subtract_last)

        # Patching
        self.patch_len = patch_len
        self.stride = stride
        self.padding_patch = padding_patch
        patch_num = int((context_window - patch_len) / stride + 1)
        if padding_patch == 'end':  # can be modified to general case
            self.padding_patch_layer = nn.ReplicationPad1d((0, stride))
            patch_num += 1

        # Backbone
        self.backbone = TSTiEncoder(context_window, c_in, target_window, patch_num=patch_num, patch_len=patch_len,
                                    max_seq_len=max_seq_len,
                                    n_layers=n_layers, d_model=d_model, n_heads=n_heads, d_k=d_k, d_v=d_v, d_ff=d_ff,
                                    attn_dropout=attn_dropout, dropout=dropout, act=act,
                                    key_padding_mask=key_padding_mask, padding_var=padding_var,
                                    attn_mask=attn_mask, res_attention=res_attention, pre_norm=pre_norm,
                                    store_attn=store_attn,
                                    pe=pe, learn_pe=learn_pe, verbose=verbose, **kwargs)

        # Head
        self.head_nf = d_model * patch_num
        self.n_vars = c_in
        self.pretrain_head = pretrain_head
        self.head_type = head_type
        self.individual = individual

        if self.pretrain_head:
            self.head = self.create_pretrain_head(self.head_nf, c_in,
                                                  fc_dropout)  # custom head passed as a partial func with all its kwargs
        elif head_type == 'flatten':
            self.head = Flatten_Head(self.individual, self.n_vars, self.head_nf, target_window,
                                     head_dropout=head_dropout)

        # 时间修正网络
        # Patching同上
        t_dropout = 0.5
        if self.n_vars == 7:
            self.patch_len_2 = 4
            self.stride_2 = 2
        else:
            self.patch_len_2 = 4
            self.stride_2 = 4
            t_dropout = 0.4
            if target_window == 720:
                t_dropout = 0.1
        patch_num_2 = int((target_window - self.patch_len_2) / self.stride_2 + 1)
        if padding_patch == 'end':  # can be modified to general case
            self.padding_patch_layer2 = nn.ReplicationPad1d((0, self.stride_2))
            patch_num_2 += 1
        self.o = 4
        if target_window > 335:
            self.o = 8
        self.head_nf = d_model // self.o * patch_num_2
        # Agent Attention
        self.agent = AgentiEncoder(c_in, patch_num=patch_num_2, patch_len=self.patch_len_2, max_seq_len=max_seq_len,
                                   n_layers=n_layers, d_model=d_model // self.o, n_heads=n_heads, d_k=d_k, d_v=d_v, d_ff=d_ff,
                                   attn_dropout=attn_dropout, dropout=dropout, act=act, key_padding_mask=key_padding_mask,
                                   padding_var=padding_var,
                                   attn_mask=attn_mask, res_attention=res_attention, pre_norm=pre_norm, store_attn=store_attn,
                                   pe=pe, learn_pe=learn_pe, verbose=verbose, **kwargs)
        # Flatten & Linear
        self.tfl = Flatten_Head(self.individual, self.n_vars, self.head_nf, target_window, head_dropout=head_dropout)
        self.dropout_attn2 = nn.Dropout(t_dropout)
        # 融合部分
        self.sig = nn.Sequential(nn.Linear(target_window, 64),
                                 nn.ReLU(),
                                 nn.Linear(64, target_window),
                                 nn.Sigmoid())

        # 空间修正网络 - 混合变量
        s_drop = 0.7
        d = 64
        if self.n_vars > 20:
            self.c = 7
            if self.n_vars == 21:
                s_drop = 0.5
        else:
            self.c = self.n_vars
        self.sl = nn.Linear(self.n_vars, self.c)
        self.s_attn = nn.Parameter(torch.zeros(1, self.c, self.c))
        self.sl2 = nn.Linear(self.c, self.n_vars)
        self.drop = nn.Dropout(s_drop)
        self.sig2 = nn.Sequential(nn.Linear(target_window, d),
                                  nn.ReLU(),
                                  nn.Linear(d, target_window),
                                  nn.Sigmoid())

        # self.jiaoshou =  MemoryAugmented(num_nodes=7, mem_num=40, mem_dim=32, loop_times=2, fusion_factor=0.7)

    def forward(self, z):  # z: [bs x nvars x seq_len]
        # norm
        if self.revin:
            z = z.permute(0, 2, 1)
            z = self.revin_layer(z, 'norm')
            z = z.permute(0, 2, 1)

        # do patching
        if self.padding_patch == 'end':
            z = self.padding_patch_layer(z)
        z = z.unfold(dimension=-1, size=self.patch_len, step=self.stride)  # z: [bs x nvars x patch_num x patch_len]
        z = z.permute(0, 1, 3, 2)  # z: [bs x nvars x patch_len x patch_num][128,7,16,82]

        # model
        z = self.backbone(z)  # z: [ bs x nvars x d_model x patch_num][128,7,32,82]

        # z = self.jiaoshou(z.permute(0, 3, 1, 2)).permute(0, 2, 3, 1)  # z: [bs x nvars x d_model x patch_num]


        z = self.head(z)  # z: [bs x nvars x target_window][128,7,96]


        # 时间修正网络
        # Pathcing
        t = z
        if self.padding_patch == 'end':
            t = self.padding_patch_layer2(t)
        t = t.unfold(dimension=-1, size=self.patch_len_2, step=self.stride_2)  # [bs x nvars x patch_num x patch_len]
        t = t.permute(0, 1, 3, 2)  # [bs x nvars x patch_len x patch_num]
        # Embedding Positional Agent Attention
        t = self.agent(t)
        # Flatten & Linear
        t = self.tfl(t)  # [bs x nvars x target_window]
        t = self.dropout_attn2(t)
        # 融合部分
        sig = self.sig(z)

        t = z + sig * t
        # denorm
        if self.revin:
            t = t.permute(0, 2, 1)
            t = self.revin_layer(t, 'denorm')
            t = t.permute(0, 2, 1)

        # 空间修正网络
        s = z.permute(0, 2, 1)  # [bs x target_window, nvars]
        if self.n_vars > 20:
            s = self.sl(s)
        attn_mask = (torch.eye(self.c).to("cuda:0")) == 1
        attn = self.s_attn.masked_fill(attn_mask, 0)
        s = torch.matmul(s, attn)
        if self.n_vars > 20:
            s = self.sl2(s)
        s = s.permute(0, 2, 1)
        s = self.drop(s)
        sig2 = self.sig2(z)
        s = s * sig2 + z
        # denorm
        if self.revin:
            s = s.permute(0, 2, 1)
            s = self.revin_layer(s, 'denorm')
            s = s.permute(0, 2, 1)
        return (s + t) / 2


    def create_pretrain_head(self, head_nf, vars, dropout):
        return nn.Sequential(nn.Dropout(dropout),
                             nn.Conv1d(head_nf, vars, 1)
                             )


class Flatten_Head(nn.Module):
    def __init__(self, individual, n_vars, nf, target_window, head_dropout=0):
        super().__init__()

        self.individual = individual
        self.n_vars = n_vars

        if self.individual:
            self.linears = nn.ModuleList()
            self.dropouts = nn.ModuleList()
            self.flattens = nn.ModuleList()
            for i in range(self.n_vars):
                self.flattens.append(nn.Flatten(start_dim=-2))
                self.linears.append(nn.Linear(nf, target_window))
                self.dropouts.append(nn.Dropout(head_dropout))
        else:
            self.flatten = nn.Flatten(start_dim=-2)
            self.linear = nn.Linear(nf, target_window)
            self.dropout = nn.Dropout(head_dropout)

    def forward(self, x):  # x: [bs x nvars x d_model x patch_num]
        if self.individual:
            x_out = []
            for i in range(self.n_vars):
                z = self.flattens[i](x[:, i, :, :])  # z: [bs x d_model * patch_num]
                z = self.linears[i](z)  # z: [bs x target_window]
                z = self.dropouts[i](z)
                x_out.append(z)
            x = torch.stack(x_out, dim=1)  # x: [bs x nvars x target_window]
        else:
            x = self.flatten(x)
            x = self.linear(x)
            x = self.dropout(x)
        return x


class TSTiEncoder(nn.Module):  # i means channel-independent
    def __init__(self, context_window, c_in, target_window, patch_num, patch_len, max_seq_len=1024,
                 n_layers=3, d_model=128, n_heads=16, d_k=None, d_v=None,
                 d_ff=256, norm='BatchNorm', attn_dropout=0., dropout=0., act="gelu", store_attn=False,
                 key_padding_mask='auto', padding_var=None, attn_mask=None, res_attention=True, pre_norm=False,
                 pe='zeros', learn_pe=True, verbose=False, **kwargs):
        super().__init__()

        self.patch_num = patch_num
        self.patch_len = patch_len

        # Input encoding
        q_len = patch_num
        self.W_P = nn.Linear(patch_len, d_model)  # Eq 1: projection of feature vectors onto a d-dim vector space
        self.seq_len = q_len

        # Positional encoding
        self.W_pos = positional_encoding(pe, learn_pe, q_len, d_model)

        # Residual dropout
        self.dropout = nn.Dropout(dropout)

        # Encoder
        self.encoder = TSTEncoder(context_window, c_in, target_window, q_len, d_model, n_heads, d_k=d_k, d_v=d_v,
                                  d_ff=d_ff, norm=norm, attn_dropout=attn_dropout, dropout=dropout,
                                  pre_norm=pre_norm, activation=act, res_attention=res_attention, n_layers=n_layers,
                                  store_attn=store_attn)

    def forward(self, x) -> Tensor:  # x: [bs x nvars x patch_len x patch_num]

        n_vars = x.shape[1]
        # Input encoding
        x = x.permute(0, 1, 3, 2)  # x: [bs x nvars x patch_num x patch_len]
        x = self.W_P(x)  # x: [bs x nvars x patch_num x d_model]

        u = torch.reshape(x, (x.shape[0] * x.shape[1], x.shape[2], x.shape[3]))  # u: [bs * nvars x patch_num x d_model]
        u = self.dropout(u + self.W_pos)  # u: [bs * nvars x patch_num x d_model]

        # Encoder
        z = self.encoder(u)  # z: [bs * nvars x patch_num x d_model]
        z = torch.reshape(z, (-1, n_vars, z.shape[-2], z.shape[-1]))  # z: [bs x nvars x patch_num x d_model]
        z = z.permute(0, 1, 3, 2)  # z: [bs x nvars x d_model x patch_num]

        return z

